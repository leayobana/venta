                dataType: 'json',
                type: 'post',
                contentType: "application/json; charset=utf-8",
                success: function( data, textStatus, jQxhr ){
                  $('#rucNombre').val(data.nombre_o_razon_social);
                  $('#rucDireccion').val(data.direccion);
                  $('#rucNumero').val(data.ruc);
                    console.log("BIEN FUNCIONA"+JSON.stringify(data));
                },
                error: function( jqXhr, textStatus, errorThrown ){
                  console.log( "--------------------------------");
                    console.log( errorThrown );
                    console.log( "--------------------------------");
                }
            }); 
 

}



$(function(){
  $('#botoncito').on('click', function(){
    var ruc = $('#ruc').val();
    var url = 'consulta_sunat.php';
    $('.ajaxgif').removeClass('hide');
    $.ajax({
    type:'POST',
    url:url,
    data:'ruc='+ruc,
    success: function(datos_dni){
      $('.ajaxgif').addClass('hide');
      var datos = eval(datos_dni);
        var nada ='nada';
        if(datos[0]==nada){
          alert('DNI o RUC no válido o no registrado');
        }else{
          $('#numero_ruc').text(datos[0]);
          $('#nuevoCliente').val(datos[1]);
          $('#fecha_actividad').text(datos[2]);
          $('#condicion').text(datos[3]);
          $('#tipo').text(datos[4]);
          $('#estado').text(datos[5]);
          $('#fecha_inscripcion').text(datos[6]);
          $('#domicilio').text(datos[7]);
          $('#emision').text(datos[8]);
        }   
    }
  });
  return false;
  });
});
